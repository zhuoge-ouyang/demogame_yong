function normalizeTypography(value) {
  return typeof value === 'string'
    ? value
        .replace(/"([^"\n]+)"/g, '“$1”')
        .replace(/\.\.\./g, '……')
        .replace(/,/g, '，')
        .replace(/;/g, '；')
        .replace(/!/g, '！')
        .replace(/\?/g, '？')
        .replace(/\[([^\]]+)\]/g, '（$1）')
    : value
}

function restoreExpandedSchema(state, avatarState) {
  for (const [continentId, continent] of Object.entries(state)) {
    const avatarContinent = avatarState[continentId]
    const avatarNodes = avatarContinent?.levelNodes || []
    continent.levelNodes ||= []

    for (let index = 0; index < 9; index++) {
      const avatarNode = avatarNodes[index] || {}
      const node = continent.levelNodes[index] ||= structuredClone(avatarNode)
      if (typeof node.storyContent !== 'string') {
        node.storyContent = avatarNode.storyContent || ''
      }
      if (!node.opponent || typeof node.opponent !== 'object') {
        node.opponent = structuredClone(avatarNode.opponent || {
          name: '',
          identity: '',
          motivation: '',
          signatureLine: ''
        })
      }
    }
  }
}

function reviewStandardData(state) {
  for (const [continentId, stories] of Object.entries(storyOverrides)) {
    for (const [area, story] of Object.entries(stories)) {
      state[continentId].levelNodes[Number(area) - 1].storyContent = story
    }
  }

  for (const [continentId, areas] of Object.entries(copyOverrides)) {
    for (const [area, values] of Object.entries(areas)) {
      const node = state[continentId].levelNodes[Number(area) - 1]
      if (values.completion) node.completionFeedback = values.completion
      if (values.opponentLine) node.opponent.signatureLine = values.opponentLine
    }
  }

  for (const continent of Object.values(state)) {
    continent.systemDialogue.opening = normalizeTypography(continent.systemDialogue.opening)
    continent.systemDialogue.actNodes = continent.systemDialogue.actNodes.map(normalizeTypography)
    continent.bosses.forEach(boss => {
      for (const field of ['name', 'identity', 'motivation', 'signatureLine']) boss[field] = normalizeTypography(boss[field])
    })
    continent.levelNodes.forEach(node => {
      for (const field of ['name', 'storyPurpose', 'storyContent', 'entryPrompt', 'completionFeedback', 'narrativeReward']) {
        node[field] = normalizeTypography(node[field])
      }
      for (const field of ['name', 'identity', 'motivation', 'signatureLine']) {
        node.opponent[field] = normalizeTypography(node.opponent[field])
      }
    })
  }
}

