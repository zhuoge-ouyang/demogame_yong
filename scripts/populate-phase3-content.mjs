function buildContinent(raw, existing = {}) {
  return {
    systemDialogue: { opening: raw.system[0], actNodes: raw.system.slice(1) },
    bosses: raw.bosses.map(([name, identity, motivation, signatureLine], index) => ({
      name,
      identity,
      motivation,
      signatureLine,
      act: index + 1,
      areaIndex: (index + 1) * 3
    })),
    levelNodes: raw.nodes.map(([name, storyPurpose, entryPrompt, completionFeedback, narrativeReward], index) => ({
      name,
      act: Math.floor(index / 3) + 1,
      storyPurpose,
      storyContent: existing.levelNodes?.[index]?.storyContent || '',
      entryPrompt,
      completionFeedback,
      narrativeReward,
      opponent: existing.levelNodes?.[index]?.opponent || {
        name: '',
        identity: '',
        motivation: '',
        signatureLine: ''
      },
      gameplayHandoff: existing.levelNodes?.[index]?.gameplayHandoff || ''
    })),
    _meta: existing._meta || {}
  }
}

function validateContent() {
  const errors = []
  for (const [continentId, raw] of Object.entries(source)) {
    const shortCopies = [
      ...raw.system.map((value, index) => [`系统${index}`, value]),
      ...raw.bosses.map((boss, index) => [`Boss${index + 1}台词`, boss[3]]),
      ...raw.nodes.flatMap((node, index) => [
        [`区域${index + 1}进入`, node[2]],
        [`区域${index + 1}结束`, node[3]]
      ])
    ]
    for (const [label, value] of shortCopies) {
      const length = [...value].length
      if (length < 20 || length > 40) errors.push(`${continentId} ${label}: ${length}字`)
    }
    if (raw.bosses.length !== 3) errors.push(`${continentId}: Boss数量不是3`)
    if (raw.nodes.length !== 9) errors.push(`${continentId}: 区域数量不是9`)
  }
  if (errors.length) throw new Error(`阶段三内容校验失败：\n${errors.join('\n')}`)
}

validateContent()

for (const fileName of ['landing.json', 'landing-avatar.json']) {
  const filePath = path.join(rootDir, 'data', fileName)
  const existing = fs.existsSync(filePath) ? JSON.parse(fs.readFileSync(filePath, 'utf8')) : {}
  const result = Object.fromEntries(Object.entries(source).map(([id, raw]) => [id, buildContinent(raw, existing[id])]))
  fs.writeFileSync(filePath, `${JSON.stringify(result, null, 2)}\n`, 'utf8')
  console.log(`Populated ${fileName}`)
}
