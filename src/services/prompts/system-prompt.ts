// ─────────────────────────────────────────────────────────────────────────────
// 层级映射表
// ─────────────────────────────────────────────────────────────────────────────
const LAYER_MAP: Record<string, string> = {
  'base': LAYER_BASE,
  'setting': LAYER_SETTING,
  'narrative': LAYER_NARRATIVE,
}

// ─────────────────────────────────────────────────────────────────────────────
// 导出函数
// ─────────────────────────────────────────────────────────────────────────────

/**
 * 根据模块类别返回分层组装的系统提示词
 * @param category - 模块类别
 * @returns 组装后的系统提示词
 */
export function getLayeredSystemPrompt(
  category: ModuleCategory,
  options: { excludeTempleFactions?: boolean } = {}
): string {
  const config = getCategoryConfig(category)
  const layers = config.systemPromptLayers
  return layers.map(layer => {
    if (layer === 'narrative' && options.excludeTempleFactions) {
      return LAYER_NARRATIVE_WITHOUT_TEMPLE_FACTIONS
    }
    return LAYER_MAP[layer]
  }).filter(Boolean).join('\n\n')
}

/**
 * 获取完整的系统提示词（全部三层）
 * 保留此函数以保持向后兼容
 * @returns 完整的系统提示词
 */
export function getSystemPrompt(): string {
  return [LAYER_BASE, LAYER_SETTING, LAYER_NARRATIVE].join('\n\n')
}

// 导出各层常量，供外部按需使用
export { LAYER_BASE, LAYER_SETTING, LAYER_NARRATIVE }
